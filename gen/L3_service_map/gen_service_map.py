#!/usr/bin/env python3
"""
gen_service_map.py - inventory.json から L3サービスマップを生成する

生成物:
  - service_map.mmd   : Mermaid形式
  - service_map.html  : HTML表形式

Usage:
    python3 gen_service_map.py [--spec PATH] [--outdir PATH]
"""

import json
import argparse
from pathlib import Path

CATEGORY_RULES = [
    ("net",   ["gateway", "firewall", "openwrt", "wireguard", "proxy", "reverse proxy"]),
    ("ai",    ["ollama", "inference", "whisper", "voicevox", "stable diffusion", "obs", "streaming core", "auth", "monitor"]),
    ("game",  ["game server", "spigot", "minecraft", "sunshine", "streaming"]),
    ("media", ["vseeface", "avatar", "録画"]),
    ("infra", ["nas", "hypervisor", "thin client", "ntp", "stratum", "作業機"]),
]

CAT_COLORS = {
    "net":   {"fill": "#E6F1FB", "stroke": "#185FA5", "text": "#0C447C"},
    "ai":    {"fill": "#EEEDFE", "stroke": "#534AB7", "text": "#3C3489"},
    "game":  {"fill": "#EAF3DE", "stroke": "#3B6D11", "text": "#27500A"},
    "media": {"fill": "#FAEEDA", "stroke": "#BA7517", "text": "#633806"},
    "infra": {"fill": "#F1EFE8", "stroke": "#5F5E5A", "text": "#2C2C2A"},
}

CAT_LABEL = {
    "net":   "ネットワーク",
    "ai":    "AI / 配信コア",
    "game":  "ゲーム",
    "media": "メディア / 配信",
    "infra": "インフラ",
}

def classify(node):
    role = node.get("role", "").lower()
    for cat, keywords in CATEGORY_RULES:
        if any(k in role for k in keywords):
            return cat
    return "infra"

def node_id(hostname):
    return hostname.upper().replace("-", "_")

# ============================================================
# Mermaid生成
# ============================================================
def generate_mmd(nodes, out_path):
    lines = [
        "graph LR",
        "",
        "%% Auto-generated by gen_service_map.py",
        "%% Source: inventory.json",
        "",
    ]

    cats = {}
    for node in nodes:
        cat = classify(node)
        cats.setdefault(cat, []).append(node)

    for cat, cat_nodes in cats.items():
        label = CAT_LABEL.get(cat, cat)
        lines.append(f'subgraph {cat.upper()} ["{label}"]')
        for node in cat_nodes:
            nid = node_id(node["hostname"])
            services = node.get("services", [])
            svc_lines = []
            for svc in services:
                if svc.get("status") == "legacy":
                    continue
                port = svc.get("port")
                port_str = f":{port}" if port else ""
                pub = "pub" if svc.get("public") else "loc"
                svc_lines.append(f"[{pub}] {svc['name']}{port_str}")
            svc_text = "\\n".join(svc_lines) if svc_lines else "no services"
            ip_short = node.get("ip", "").replace("192.168.1.", ".")
            lines.append(f'    {nid}["{node["hostname"]} {ip_short}\\n{"─"*20}\\n{svc_text}"]')
        lines.append("end")
        lines.append("")

    lines.append("%% スタイル")
    for cat, cat_nodes in cats.items():
        c = CAT_COLORS.get(cat, CAT_COLORS["infra"])
        ids = ",".join(node_id(n["hostname"]) for n in cat_nodes)
        lines.append(f"classDef {cat}_style fill:{c['fill']},stroke:{c['stroke']},color:{c['text']}")
        lines.append(f"class {ids} {cat}_style")

    out_path.write_text("\n".join(lines), encoding="utf-8")
    print(f"Generated: {out_path}")

# ============================================================
# HTML生成
# ============================================================
def generate_html(nodes, out_path):
    cats = {}
    for node in nodes:
        cat = classify(node)
        cats.setdefault(cat, []).append(node)

    table_rows = ""
    for cat, cat_nodes in cats.items():
        c = CAT_COLORS.get(cat, CAT_COLORS["infra"])
        label = CAT_LABEL.get(cat, cat)

        total_rows = sum(
            max(len(n.get("services", [])) + len(n.get("system_services", [])), 1)
            for n in cat_nodes
        )
        first_cat = True

        for node in cat_nodes:
            services = node.get("services", [])
            system_services = node.get("system_services", [])
            all_svcs = [("svc", s) for s in services] + [("sys", s) for s in system_services]
            if not all_svcs:
                all_svcs = [("none", None)]

            ip_short = node.get("ip", "").replace("192.168.1.", ".")
            hw = node.get("hardware", "")
            node_rowspan = len(all_svcs)
            first_node = True

            for stype, svc in all_svcs:
                cat_td = ""
                if first_cat:
                    cat_td = (
                        f'<td rowspan="{total_rows}" style="'
                        f'border-left:4px solid {c["stroke"]};'
                        f'background:{c["fill"]};color:{c["text"]};'
                        f'font-weight:600;font-size:11px;text-align:center;'
                        f'writing-mode:vertical-rl;transform:rotate(180deg);'
                        f'padding:8px 4px;white-space:nowrap;">{label}</td>'
                    )
                    first_cat = False

                node_td = ""
                if first_node:
                    node_td = (
                        f'<td rowspan="{node_rowspan}" style="'
                        f'vertical-align:top;padding:8px;'
                        f'border-right:1px solid #EDF2F7;min-width:120px;">'
                        f'<div style="font-weight:700;font-size:13px;font-family:monospace;">{node["hostname"]}</div>'
                        f'<div style="font-size:11px;color:#666;font-family:monospace;">{ip_short}</div>'
                        f'<div style="font-size:10px;color:#999;margin-top:3px;">{hw}</div>'
                        f'</td>'
                    )
                    first_node = False

                if stype == "none":
                    table_rows += f"<tr>{cat_td}{node_td}<td colspan='5' style='color:#bbb;font-size:11px;padding:8px;'>—</td></tr>\n"
                    continue

                if stype == "svc":
                    is_legacy = svc.get("status") == "legacy"
                    is_public = svc.get("public", False)
                    port = svc.get("port", "")
                    port_str = str(port) if port else "—"
                    proto = svc.get("protocol") or "—"
                    pub_badge = (
                        '<span style="background:#E53E3E;color:white;padding:1px 5px;border-radius:3px;font-size:10px;">公開</span>'
                        if is_public else
                        '<span style="background:#718096;color:white;padding:1px 5px;border-radius:3px;font-size:10px;">内部</span>'
                    )
                    legacy_badge = (
                        ' <span style="background:#D69E2E;color:white;padding:1px 5px;border-radius:3px;font-size:10px;">legacy</span>'
                        if is_legacy else ""
                    )
                    row_bg = "background:#fffbf0;" if is_legacy else ""
                    row_op = "opacity:0.6;" if is_legacy else ""
                    table_rows += (
                        f'<tr style="{row_bg}{row_op}">'
                        f'{cat_td}{node_td}'
                        f'<td style="font-family:monospace;font-size:12px;padding:6px 8px;">{svc["name"]}</td>'
                        f'<td style="font-family:monospace;font-size:12px;padding:6px 8px;text-align:right;">{port_str}</td>'
                        f'<td style="font-size:11px;color:#666;padding:6px 8px;">{proto}</td>'
                        f'<td style="padding:6px 8px;">{pub_badge}{legacy_badge}</td>'
                        f'<td style="font-size:11px;padding:6px 8px;color:#444;">{svc.get("description","")}</td>'
                        f'</tr>\n'
                    )
                else:
                    table_rows += (
                        f'<tr style="background:#F7FAFC;">'
                        f'{cat_td}{node_td}'
                        f'<td style="font-family:monospace;font-size:12px;padding:6px 8px;color:#888;">⚙ {svc["name"]}</td>'
                        f'<td style="padding:6px 8px;text-align:right;color:#aaa;">—</td>'
                        f'<td style="font-size:11px;color:#888;padding:6px 8px;">{svc.get("type","—")}</td>'
                        f'<td style="padding:6px 8px;"><span style="background:#4A5568;color:white;padding:1px 5px;border-radius:3px;font-size:10px;">system</span></td>'
                        f'<td style="font-size:11px;padding:6px 8px;color:#666;">{svc.get("description","")}</td>'
                        f'</tr>\n'
                    )

    html = f"""<!DOCTYPE html>
<html lang="ja">
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>9-WORLDS L3 Service Map</title>
<style>
  @import url('https://fonts.googleapis.com/css2?family=Share+Tech+Mono&display=swap');
  body {{
    font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', sans-serif;
    background: #f7f8fa;
    margin: 0;
    padding: 24px;
    color: #2d3748;
  }}
  h1 {{
    font-family: 'Share Tech Mono', monospace;
    font-size: 1.3rem;
    color: #1a202c;
    margin-bottom: 4px;
  }}
  .subtitle {{ font-size: 12px; color: #718096; margin-bottom: 20px; }}
  table {{
    width: 100%;
    border-collapse: collapse;
    background: white;
    border-radius: 8px;
    overflow: hidden;
    box-shadow: 0 1px 4px rgba(0,0,0,0.08);
  }}
  thead th {{
    background: #2d3748;
    color: white;
    padding: 10px 8px;
    text-align: left;
    font-size: 11px;
    font-weight: 600;
    letter-spacing: 0.05em;
    text-transform: uppercase;
  }}
  tbody tr {{ border-bottom: 1px solid #EDF2F7; }}
  tbody tr:hover {{ background: #F7FAFC !important; }}
  .legend {{
    display: flex;
    gap: 16px;
    margin-top: 12px;
    font-size: 11px;
    color: #718096;
    flex-wrap: wrap;
    align-items: center;
  }}
</style>
</head>
<body>
<h1>// 9-WORLDS L3 SERVICE MAP</h1>
<div class="subtitle">Auto-generated from inventory.json</div>
<table>
  <thead>
    <tr>
      <th style="width:50px;">カテゴリ</th>
      <th style="width:130px;">ノード</th>
      <th style="width:150px;">サービス名</th>
      <th style="width:55px;">ポート</th>
      <th style="width:70px;">プロトコル</th>
      <th style="width:90px;">公開</th>
      <th>説明</th>
    </tr>
  </thead>
  <tbody>
{table_rows}
  </tbody>
</table>
<div class="legend">
  <span style="background:#E53E3E;color:white;padding:1px 6px;border-radius:3px;font-size:10px;">公開</span> WAN側から到達可能 &nbsp;
  <span style="background:#718096;color:white;padding:1px 6px;border-radius:3px;font-size:10px;">内部</span> LAN内部のみ &nbsp;
  <span style="background:#D69E2E;color:white;padding:1px 6px;border-radius:3px;font-size:10px;">legacy</span> 現在未使用 &nbsp;
  <span style="background:#4A5568;color:white;padding:1px 6px;border-radius:3px;font-size:10px;">system</span> systemdサービス（ポートなし）
</div>
</body>
</html>"""

    out_path.write_text(html, encoding="utf-8")
    print(f"Generated: {out_path}")

# ============================================================
# メイン
# ============================================================
def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--spec",   default="../inventory.json")
    parser.add_argument("--outdir", default="../L3_service_map")
    args = parser.parse_args()

    spec_path = Path(args.spec)
    out_dir   = Path(args.outdir)
    out_dir.mkdir(parents=True, exist_ok=True)

    if not spec_path.exists():
        print(f"Error: {spec_path} not found")
        exit(1)

    nodes = json.loads(spec_path.read_text(encoding="utf-8"))
    generate_mmd(nodes,  out_dir / "service_map.mmd")
    generate_html(nodes, out_dir / "service_map.html")

if __name__ == "__main__":
    main()
