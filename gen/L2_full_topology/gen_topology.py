import os
#!/usr/bin/env python3
"""
gen_topology.py - nine_worlds_spec.json から full_topology.mmd を生成する

Usage:
    python3 gen_topology.py [--spec PATH] [--out PATH]

Defaults:
    --spec  ../nine_worlds_spec.json
    --out   ../full_topology.mmd
"""

import json
import argparse
from pathlib import Path

# ============================================================
# ノードをカテゴリに分類するルール
# JSONの "role" フィールドのキーワードで判定
# ============================================================
CATEGORY_RULES = [
    ("net",     ["gateway", "firewall", "openwrt", "wireguard", "proxy", "reverse proxy"]),
    ("ai",      ["ollama", "inference", "whisper", "voicevox", "stable diffusion", "obs", "streaming core", "auth", "monitor"]),
    ("game",    ["game server", "spigot", "minecraft", "sunshine", "streaming"]),
    ("media",   ["vseeface", "avatar", "録画"]),
    ("infra",   ["nas", "hypervisor", "thin client", "ntp", "stratum", "作業機"]),
]

# 静的ノード（JSONに含まれない外部サービス・デバイス）
STATIC_NODES = {
    "cloud": [
        ('INET',    'Internet / WAN',              'wan'),
        ('MYDNS',   'MyDNS\\nDDNS: 64x.jp',        'ext'),
        ('NICT',    'NICT NTP\\nntp-b3.nict.go.jp', 'ext'),
        ('GAS',     'Google Apps Script\\nBluesky bot', 'ext'),
        ('BSKY',    'Bluesky',                      'ext'),
        ('YOUTUBE', 'YouTube\\nLive Streaming',      'wan'),
    ],
    "sensor": [
        ('TEMPMETER', 'tempmeter .156\\nESP32\\n環境センサー', 'ext'),
        ('GPS',       'GPSモジュール\\nNMEA + PPS ±105ns',    'ext'),
    ],
    "net_extra": [
        ('W3200',  'w3200 .2\\nAP / OpenWrt\\nWi-Fi',    'net'),
        ('MUTTER', 'mutter .2.2\\nWireGuard Server',      'net'),
    ],
}

# 静的な接続定義（JSONから自動生成できない関係）
STATIC_EDGES = """
%% --- WAN・公開ポート ---
INET -->|WAN| R5S
INET -->|":80 / :11435"| BIFROST
INET -->|":25565 / :34197"| GLADSHEIM
INET -->|":7777"| FJOLNIR
INET -->|":51820 UDP"| MUTTER
MYDNS -.->|DDNS更新| INET

%% --- ルーター → 各ノード (JSONから自動生成) ---
{router_edges}
%% --- 時刻同期 ---
NICT -.->|stratum 1 参照| NTP
GPS -.->|NMEA + PPS| NTP

%% --- データフロー ---
GAS -->|"Bearer token :11435"| BIFROST
BIFROST -->|proxy| DURANDAL
GAS -->|post| BSKY
MJOLNIR -->|NDI映像| DURANDAL
MJOLNIR -->|Moonlight| FJOLNIR
DURANDAL ==>|RTMP| YOUTUBE
TEMPMETER -->|センサーデータ| BIFROST
"""

STYLE_DEFS = """
classDef net_style     fill:#E6F1FB,stroke:#185FA5,color:#0C447C
classDef ai_style      fill:#EEEDFE,stroke:#534AB7,color:#3C3489
classDef game_style    fill:#EAF3DE,stroke:#3B6D11,color:#27500A
classDef media_style   fill:#FAEEDA,stroke:#BA7517,color:#633806
classDef infra_style   fill:#F1EFE8,stroke:#5F5E5A,color:#2C2C2A
classDef ext_style     fill:#E1F5EE,stroke:#0F6E56,color:#04342C
classDef wan_style     fill:#FCEBEB,stroke:#A32D2D,color:#501313
classDef offline_style fill:#eeeeee,stroke:#aaaaaa,color:#888888
"""


def classify(node: dict) -> str:
    """roleフィールドからカテゴリを決定する"""
    role = node.get("role", "").lower()
    for cat, keywords in CATEGORY_RULES:
        if any(k in role for k in keywords):
            return cat
    return "infra"


def node_id(hostname: str) -> str:
    """ホスト名をMermaid IDに変換（大文字・記号除去）"""
    return hostname.upper().replace("-", "_").replace(".", "_")


def node_label(node: dict) -> str:
    """ノードの表示ラベルを生成"""
    h = node["hostname"]
    ip_part = node.get("ip", "").replace("192.168.1.", ".")
    hw = node.get("hardware", "")
    role = node.get("role", "")

    lines = [f"{h}  {ip_part}"]
    if hw:
        lines.append(hw)
    if role:
        # roleが長い場合は先頭部分だけ
        lines.append(role[:40] + ("…" if len(role) > 40 else ""))

    return "\\n".join(lines)


def is_offline(node: dict) -> bool:
    return node.get("status", "") == "offline_or_error"


def generate(spec_path: Path, out_path: Path):
    nodes = json.loads(spec_path.read_text(encoding="utf-8"))

    # カテゴリ別に分類
    cats = {c: [] for c in ["net", "ai", "game", "media", "infra"]}
    for node in nodes:
        # センサー・マイコン類はスキップ（静的ノードで定義）
        if node["hostname"] in ("tempmeter", "ntp_skip"):
            continue
        cat = classify(node)
        cats[cat].append(node)

    lines = []
    lines.append("graph TD")
    lines.append("")
    lines.append("%% Auto-generated by gen_topology.py — DO NOT EDIT MANUALLY")
    lines.append(f"%% Source: {os.path.basename(spec_path)}")
    lines.append("")

    # ── CLOUD subgraph ──
    lines.append('subgraph CLOUD ["☁️ Internet / Cloud"]')
    for nid, label, _ in STATIC_NODES["cloud"]:
        lines.append(f"    {nid}[{label}]")
    lines.append("end")
    lines.append("")

    # ── LAN subgraph ──
    lines.append('subgraph NET ["🔵 Network — 192.168.1.x"]')
    lines.append("")

    # ルーター / AP
    lines.append('    subgraph ROUTER_LAYER ["ルーター / AP"]')
    for node in cats["net"]:
        nid = node_id(node["hostname"])
        label = node_label(node)
        lines.append(f'        {nid}["{label}"]')
    for nid, label, _ in STATIC_NODES["net_extra"]:
        lines.append(f'        {nid}["{label}"]')
    lines.append("    end")
    lines.append("")

    # WireGuard
    lines.append('    subgraph WIREGUARD ["WireGuard 192.168.2.x"]')
    lines.append('        MUTTER["mutter .2.2\\nWireGuard Server"]')
    lines.append("    end")
    lines.append("")

    # AI / 配信コア
    if cats["ai"]:
        lines.append('    subgraph AI_CORE ["🟣 AI / 配信コア"]')
        for node in cats["ai"]:
            nid = node_id(node["hostname"])
            label = node_label(node)
            lines.append(f'        {nid}["{label}"]')
        lines.append("    end")
        lines.append("")

    # ゲーム / 配信サブ
    if cats["game"] or cats["media"]:
        lines.append('    subgraph GAME ["🟢 ゲーム / 配信サブ"]')
        for node in cats["game"] + cats["media"]:
            nid = node_id(node["hostname"])
            label = node_label(node)
            suffix = '["' + label + '"]' if not is_offline(node) else '["' + label + '\\n⚫ OFFLINE"]'
            lines.append(f"        {nid}{suffix}")
        lines.append("    end")
        lines.append("")

    # インフラ
    if cats["infra"]:
        lines.append('    subgraph INFRA ["⚫ インフラ / ストレージ / 時刻"]')
        for node in cats["infra"]:
            nid = node_id(node["hostname"])
            label = node_label(node)
            suffix = '["' + label + '"]' if not is_offline(node) else '["' + label + '\\n⚫ OFFLINE"]'
            lines.append(f"        {nid}{suffix}")
        lines.append("    end")
        lines.append("")

    # センサー
    lines.append('    subgraph SENSOR ["🟡 センサー / デバイス"]')
    for nid, label, _ in STATIC_NODES["sensor"]:
        lines.append(f"        {nid}[{label}]")
    lines.append("    end")
    lines.append("")

    lines.append("end")
    lines.append("")

    # ── 接続 ──
    lines.append("%% ============================================================")
    lines.append("%% 接続")
    lines.append("%% ============================================================")
    lines.append("")

    # ルーター→各ノードをJSONから自動生成
    router_edges = []
    skip = {"r5s", "w3200", "mutter", "tempmeter"}
    for node in nodes:
        if node["hostname"] in skip:
            continue
        nid = node_id(node["hostname"])
        router_edges.append(f"R5S --- {nid}")
    router_edges.append("R5S --- W3200")
    router_edges.append("R5S --- TEMPMETER")
    router_edges.append("R5S -.->|WireGuard tunnel| MUTTER")

    edges_block = STATIC_EDGES.format(
        router_edges="\n".join(router_edges)
    )
    lines.append(edges_block.strip())
    lines.append("")

    # ── スタイル ──
    lines.append("%% ============================================================")
    lines.append("%% スタイル")
    lines.append("%% ============================================================")
    lines.append(STYLE_DEFS.strip())
    lines.append("")

    # class割り当て（カテゴリ→スタイル）
    cat_style = {
        "net":   "net_style",
        "ai":    "ai_style",
        "game":  "game_style",
        "media": "media_style",
        "infra": "infra_style",
    }
    for cat, style in cat_style.items():
        ids = [node_id(n["hostname"]) for n in cats[cat]]
        if ids:
            lines.append(f"class {','.join(ids)} {style}")

    # 静的ノードのスタイル
    lines.append("class R5S,W3200,MUTTER net_style")
    lines.append("class INET,YOUTUBE wan_style")
    lines.append("class MYDNS,NICT,GAS,BSKY,TEMPMETER,GPS ext_style")

    # offline
    offline_ids = [node_id(n["hostname"]) for n in nodes if is_offline(n)]
    if offline_ids:
        lines.append(f"class {','.join(offline_ids)} offline_style")

    lines.append("")

    out_path.write_text("\n".join(lines), encoding="utf-8")
    print(f"Generated: {out_path}")


def main():
    parser = argparse.ArgumentParser(description="Generate full_topology.mmd from nine_worlds_spec.json")
    parser.add_argument("--spec", default="../inventory.json", help="Path to nine_worlds_spec.json")
    parser.add_argument("--out",  default="../full_topology.mmd",     help="Output path for full_topology.mmd")
    args = parser.parse_args()

    spec_path = Path(args.spec)
    out_path  = Path(args.out)

    if not spec_path.exists():
        print(f"Error: spec file not found: {spec_path}")
        exit(1)

    generate(spec_path, out_path)


if __name__ == "__main__":
    main()
